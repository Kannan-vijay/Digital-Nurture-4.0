
public class TypeCasting {
    public static void main(String[] args) {
        double num = 5.6f;
        System.out.println(num + " After Converting Double into Int Value is : " + (int) num);
        int num1 = 7;
        System.out.println(num1 + " After Converting Int into Double Value is : " + (num1 * 1.1));

    }
}

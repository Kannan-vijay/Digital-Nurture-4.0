

public class DataType {
    public static void main(String[] args) {
        int a = 10;
        float b = 5.0f;
        double c = 5.00000000000;
        char d = 'A';
        boolean e = true;

        System.out.println("The Integer Value : "+a);
        System.out.println("The Float Value : "+b);
        System.out.println("The double Value : "+c);
        System.out.println("The char Value : "+d);
        System.out.println("The boolean Value : "+e);
    }
}
